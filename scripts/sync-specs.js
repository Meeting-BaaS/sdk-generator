// ── Checksum Helpers ─────────────────────────────────────────────────────────

function sha256(content) {
  return crypto.createHash("sha256").update(content, "utf-8").digest("hex")
}

function loadChecksums() {
  try {
    return JSON.parse(fs.readFileSync(CHECKSUMS_FILE, "utf-8"))
  } catch {
    return { specs: {}, references: {} }
  }
}

function saveChecksums(data) {
  data.updatedAt = new Date().toISOString()
  fs.writeFileSync(CHECKSUMS_FILE, JSON.stringify(data, null, 2) + "\n", "utf-8")
}

// ── HTTP Helpers ─────────────────────────────────────────────────────────────

/**
 * Fetch content from URL (follows redirects, supports HTTP and HTTPS)
 */
function fetchUrl(url) {
  return new Promise((resolve, reject) => {
    const makeRequest = (requestUrl) => {
      const client = requestUrl.startsWith("https") ? https : http
      client
        .get(requestUrl, (res) => {
          // Handle redirects
          if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
            makeRequest(res.headers.location)
            return
          }

          if (res.statusCode !== 200) {
            reject(new Error(`HTTP ${res.statusCode}: ${res.statusMessage}`))
            return
          }

          let data = ""
          res.on("data", (chunk) => (data += chunk))
          res.on("end", () => resolve(data))
          res.on("error", reject)
        })
        .on("error", reject)
    }

    makeRequest(url)
  })
}

/**
 * Validate spec content (basic check)
 */
function validateSpec(content, format) {
  try {
    if (format === "json") {
      const parsed = JSON.parse(content)
      // Check for OpenAPI or AsyncAPI markers
      if (!parsed.openapi && !parsed.asyncapi && !parsed.swagger) {
        return { valid: false, error: "Missing openapi/asyncapi/swagger version field" }
      }
      return { valid: true, version: parsed.openapi || parsed.asyncapi || parsed.swagger }
    } else if (format === "yaml") {
      // Basic YAML validation - check for openapi/asyncapi field
      if (
        !content.includes("openapi:") &&
        !content.includes("swagger:") &&
        !content.includes("asyncapi:")
      ) {
        return { valid: false, error: "Missing openapi/asyncapi/swagger version field" }
      }
      const versionMatch = content.match(/(?:openapi|asyncapi):\s*['"]?([^'"\s]+)['"]?/)
      return { valid: true, version: versionMatch ? versionMatch[1] : "unknown" }
    } else if (format === "typescript") {
      // TypeScript SDK file - check for expected exports
      if (!content.includes("interface") && !content.includes("type")) {
        return { valid: false, error: "Missing TypeScript interface/type definitions" }
      }
      return { valid: true, version: "sdk" }
    }
    return { valid: false, error: "Unknown format" }
  } catch (e) {
    return { valid: false, error: e.message }
  }
}

// ── Reference Checking ───────────────────────────────────────────────────────

/**
 * Check a reference URL for upstream changes.
 * For npm-version type, extracts and compares the package version.
 * For raw type, hashes the content and compares.
 */
async function checkReference(name, ref, checksumData) {
  try {
    const content = await fetchUrl(ref.url)

    let trackingValue
    let displayValue

    if (ref.type === "npm-version") {
      // Extract version from npm registry JSON response
      const parsed = JSON.parse(content)
      trackingValue = parsed.version
      displayValue = `v${parsed.version}`
    } else {
      // Hash raw content
      trackingValue = sha256(content)
      displayValue = trackingValue.slice(0, 12)
    }

    const oldValue = checksumData.references[name]?.trackingValue
    const changed = oldValue != null && trackingValue !== oldValue
    const isNew = oldValue == null

    checksumData.references[name] = {
      trackingValue,
      url: ref.url,
      note: ref.note,
      type: ref.type || "sha256",
      checkedAt: new Date().toISOString()
    }

    if (changed) {
      const oldDisplay = ref.type === "npm-version"
        ? `v${oldValue}`
        : oldValue.slice(0, 12)
      console.log(`    ⚠️  Reference CHANGED: ${ref.note} (${oldDisplay} → ${displayValue})`)
      console.log(`       Manual spec may need updating`)
    } else if (isNew) {
      console.log(`    📌 Reference tracked: ${ref.note} (${displayValue})`)
    } else {
      console.log(`    ✅ Reference unchanged: ${ref.note} (${displayValue})`)
    }

    return { changed, isNew }
  } catch (e) {
    console.log(`    ⚠️  Reference check failed: ${ref.note} — ${e.message}`)
    return { changed: false, error: e.message }
  }
}

// ── Sync Logic ───────────────────────────────────────────────────────────────

/**
 * Sync a single spec. Computes checksums and detects changes.
 */
async function syncSpec(name, config, checksumData, options = {}) {
  const outputPath = path.join(__dirname, "..", config.output)
  const checkOnly = options.checkOnly || false
  const changedSpecs = options.changedSpecs || new Set()

  // ── Manual specs ─────────────────────────────────────────────────────────
  if (config.manual) {
    // Check if manual spec file exists
    if (!fs.existsSync(outputPath)) {
      console.log(`  ⚠️  ${name}: Manual spec missing at ${config.output}`)
      return { status: "missing", reason: "manual spec not found" }
    }

    // Track local file checksum
    const localContent = fs.readFileSync(outputPath, "utf-8")
    const localHash = sha256(localContent)
    checksumData.specs[name] = {
      sha256: localHash,
      manual: true,
      checkedAt: new Date().toISOString(),
      note: config.note
    }

    // Check if parent specs changed (dependsOn)
    let parentChanged = false
    if (config.dependsOn) {
      const changedParents = config.dependsOn.filter((dep) => changedSpecs.has(dep))
      if (changedParents.length > 0) {
        parentChanged = true
        console.log(
          `  ⚠️  ${name}: Input(s) changed [${changedParents.join(", ")}] — may need regeneration`
        )
      }
    }

    // Check reference URL for upstream changes
    let refChanged = false
    if (config.reference) {
      console.log(`  🔍 ${name}: Checking upstream reference...`)
      const refResult = await checkReference(name, config.reference, checksumData)
      refChanged = refResult.changed
    }

    if (!parentChanged && !refChanged) {
      console.log(`  ⏭️  ${name}: Manual spec — ${config.note}`)
    }

    return {
      status: parentChanged || refChanged ? "needs-review" : "skipped",
      reason: parentChanged
        ? "parent spec changed"
        : refChanged
          ? "upstream reference changed"
          : "manual",
      changed: false
    }
  }

  // ── Auto-sync specs ──────────────────────────────────────────────────────
  console.log(`  📥 ${name}: Fetching from ${config.url}`)

  try {
    const content = await fetchUrl(config.url)

    // Validate
    const validation = validateSpec(content, config.format)
    if (!validation.valid) {
      console.log(`  ❌ ${name}: Invalid spec — ${validation.error}`)
      return { status: "error", error: validation.error }
    }

    const newHash = sha256(content)
    const oldHash = checksumData.specs[name]?.sha256
    const changed = newHash !== oldHash

    // Update checksum data
    checksumData.specs[name] = {
      sha256: newHash,
      url: config.url,
      syncedAt: new Date().toISOString(),
      size: content.length
    }

    if (checkOnly) {
      const icon = changed ? "🔄" : "✅"
      const label = changed ? "CHANGED" : "Unchanged"
      console.log(`  ${icon} ${name}: ${label} (${validation.version})`)
      return { status: changed ? "changed" : "unchanged", changed, version: validation.version }
    }

    // Write spec to disk
    const dir = path.dirname(outputPath)
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true })
    }
    fs.writeFileSync(outputPath, content, "utf-8")

    const icon = changed ? "🔄" : "✅"
    const label = changed ? "Updated" : "Saved"
    console.log(`  ${icon} ${name}: ${label} → ${config.output} (${validation.version})`)

    return { status: "success", changed, version: validation.version }
  } catch (e) {
    console.log(`  ❌ ${name}: Failed — ${e.message}`)
    return { status: "error", error: e.message }
  }
}

// ── Validate-Only Mode ───────────────────────────────────────────────────────

/**
 * Validate existing specs without downloading
 */
async function validateOnly() {
  console.log("🔍 Validating existing specs...\n")

  let allValid = true

  for (const [name, config] of Object.entries(SPEC_SOURCES)) {
    const outputPath = path.join(__dirname, "..", config.output)

    if (!fs.existsSync(outputPath)) {
      if (config.manual) {
        console.log(`  ⚠️  ${name}: Manual spec missing`)
      } else {
        console.log(`  ❌ ${name}: Spec not found — run sync first`)
        allValid = false
      }
      continue
    }

    // Skip validation for manual specs - they're manually maintained
    if (config.manual) {
      console.log(`  ⏭️  ${name}: Manual spec (skipped)`)
      continue
    }

    const content = fs.readFileSync(outputPath, "utf-8")
    // Determine format from config or file extension
    let format = config.format
    if (!format) {
      if (outputPath.endsWith(".json")) format = "json"
      else if (outputPath.endsWith(".ts")) format = "typescript"
      else format = "yaml"
    }
    const validation = validateSpec(content, format)

    if (validation.valid) {
      console.log(`  ✅ ${name}: Valid (${validation.version})`)
    } else {
      console.log(`  ❌ ${name}: Invalid — ${validation.error}`)
      allValid = false
    }
  }

  return allValid
}

// ── Main ─────────────────────────────────────────────────────────────────────

async function main() {
  const args = process.argv.slice(2)

  // Parse arguments
  const validateOnlyMode = args.includes("--validate-only")
  const checkOnlyMode = args.includes("--check-only")
  const providerIndex = args.indexOf("--provider")
  const specificProvider = providerIndex >= 0 ? args[providerIndex + 1] : null

  if (validateOnlyMode) {
    const valid = await validateOnly()
    process.exit(valid ? 0 : 1)
  }

  // Load existing checksums
  const checksumData = loadChecksums()
  if (!checksumData.specs) checksumData.specs = {}
  if (!checksumData.references) checksumData.references = {}

  // Track which specs changed in this run (for dependsOn resolution)
  const changedSpecs = new Set()

  if (checkOnlyMode) {
    console.log("🔍 Checking for spec updates (no files will be written)...\n")
  } else {
    console.log("🔄 Syncing OpenAPI specs from official sources...\n")
  }

  const results = {}

  if (specificProvider) {
    // Sync specific provider
    if (!SPEC_SOURCES[specificProvider]) {
      console.error(`❌ Unknown provider: ${specificProvider}`)
      console.error(`Available providers: ${Object.keys(SPEC_SOURCES).join(", ")}`)
      process.exit(1)
    }
    const result = await syncSpec(specificProvider, SPEC_SOURCES[specificProvider], checksumData, {
      checkOnly: checkOnlyMode,
      changedSpecs
    })
    results[specificProvider] = result
    if (result.changed) changedSpecs.add(specificProvider)
  } else {
    // Sync all providers (auto-sync first, then manual — dependsOn ordering)
    for (const [name, config] of Object.entries(SPEC_SOURCES)) {
      const result = await syncSpec(name, config, checksumData, {
        checkOnly: checkOnlyMode,
        changedSpecs
      })
      results[name] = result
      if (result.changed) changedSpecs.add(name)
    }
  }

  // Save checksums
  if (!checkOnlyMode) {
    saveChecksums(checksumData)
    console.log(`\n  💾 Checksums saved to specs/.checksums.json`)
  }

  // ── Summary ────────────────────────────────────────────────────────────────

  const changed = Object.entries(results)
    .filter(([, r]) => r.changed)
    .map(([n]) => n)
  const needsReview = Object.entries(results)
    .filter(([, r]) => r.status === "needs-review")
    .map(([n]) => n)
  const errors = Object.entries(results)
    .filter(([, r]) => r.status === "error")
    .map(([n]) => n)
  const success = Object.entries(results)
    .filter(([, r]) => r.status === "success" || r.status === "unchanged")
    .map(([n]) => n)
  const skipped = Object.entries(results)
    .filter(([, r]) => r.status === "skipped")
    .map(([n]) => n)

  console.log("\n📊 Summary:")

  if (changed.length > 0) {
    console.log(`  🔄 Changed (${changed.length}): ${changed.join(", ")}`)
  }
  if (needsReview.length > 0) {
    console.log(`  ⚠️  Needs review (${needsReview.length}): ${needsReview.join(", ")}`)
  }
  if (errors.length > 0) {
    console.log(`  ❌ Errors (${errors.length}): ${errors.join(", ")}`)
  }

  const unchangedCount = success.length + skipped.length
  if (changed.length === 0 && needsReview.length === 0 && errors.length === 0) {
    console.log(`  ✅ All ${unchangedCount} specs up to date`)
  } else if (unchangedCount > 0) {
    console.log(`  ✅ Unchanged: ${unchangedCount}`)
  }

  // Exit codes
  if (checkOnlyMode) {
    // Exit 0 = no changes, Exit 2 = changes detected
    process.exit(changed.length > 0 || needsReview.length > 0 ? 2 : 0)
  }
  if (errors.length > 0) {
    process.exit(1)
  }
}

// Run
main().catch((e) => {
  console.error("Fatal error:", e)
  process.exit(1)
})
