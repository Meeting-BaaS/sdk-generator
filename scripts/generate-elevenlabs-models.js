#!/usr/bin/env node
/**
 * Generate ElevenLabs model constants from OpenAPI spec
 *
 * Extracts model IDs from the BodySpeechToTextV1SpeechToTextPost.model_id enum
 * in the ElevenLabs OpenAPI spec and generates TypeScript constants.
 *
 * Run: node scripts/generate-elevenlabs-models.js
 */

const fs = require("fs")
const path = require("path")

const SPEC_PATH = path.join(__dirname, "../specs/elevenlabs-openapi.json")
const OUTPUT_PATH = path.join(__dirname, "../src/generated/elevenlabs/models.ts")

function main() {
  console.log("📦 Generating ElevenLabs model constants from OpenAPI spec...")

  if (!fs.existsSync(SPEC_PATH)) {
    console.error(`❌ OpenAPI spec not found: ${SPEC_PATH}`)
    console.error("   Run 'pnpm openapi:sync:elevenlabs' first to download the spec.")
    process.exit(1)
  }

  const spec = JSON.parse(fs.readFileSync(SPEC_PATH, "utf-8"))

  // Extract model_id enum from the Body schema
  const bodySchema = spec.components?.schemas?.["Body_Speech_to_Text_v1_speech_to_text_post"]
  const modelIdProp = bodySchema?.properties?.model_id
  const modelEnum = modelIdProp?.enum

  if (!modelEnum || modelEnum.length === 0) {
    console.error("❌ Could not find model_id enum in spec")
    process.exit(1)
  }

  console.log(`   Found ${modelEnum.length} models: ${modelEnum.join(", ")}`)

  // Define model metadata
  const models = modelEnum.map((id) => {
    const labels = {
      scribe_v1: "Scribe V1",
      scribe_v2: "Scribe V2"
    }
    return {
      id,
      name: labels[id] || id,
      supportsRealtime: id === "scribe_v2" // Only V2 supports realtime
    }
  })

  const realtimeModels = [{ id: "scribe_v2_realtime", name: "Scribe V2 Realtime" }]

  const toIdentifier = (id) => id.replace(/-/g, "_")

  const output = `/**
 * Generated from ElevenLabs OpenAPI spec - DO NOT EDIT MANUALLY
 *
 * This file is auto-generated by scripts/generate-elevenlabs-models.js
 * Run 'pnpm openapi:sync-elevenlabs-models' to regenerate.
 *
 * @generated
 */

/**
 * ElevenLabs batch transcription model IDs (from OpenAPI spec enum)
 */
export const ElevenLabsModelCodes = [
${models.map((m) => `  "${m.id}"`).join(",\n")}
] as const

/**
 * Type for ElevenLabs batch model codes
 */
export type ElevenLabsModelCode = (typeof ElevenLabsModelCodes)[number]

/**
 * ElevenLabs realtime streaming model IDs
 */
export const ElevenLabsRealtimeModelCodes = [
${realtimeModels.map((m) => `  "${m.id}"`).join(",\n")}
] as const

/**
 * Type for ElevenLabs realtime model codes
 */
export type ElevenLabsRealtimeModelCode = (typeof ElevenLabsRealtimeModelCodes)[number]

/**
 * Model ID to display name mapping
 */
export const ElevenLabsModelLabels: Record<ElevenLabsModelCode | ElevenLabsRealtimeModelCode, string> = {
${[...models, ...realtimeModels].map((m) => `  "${m.id}": "${m.name}"`).join(",\n")}
} as const

/**
 * ElevenLabs batch model constant object for autocomplete
 *
 * @example
 * \`\`\`typescript
 * import { ElevenLabsModel } from 'voice-router-dev/constants'
 *
 * { model: ElevenLabsModel.scribe_v2 }
 * \`\`\`
 */
export const ElevenLabsModel = {
${models.map((m) => `  ${toIdentifier(m.id)}: "${m.id}"`).join(",\n")}
} as const satisfies Record<string, ElevenLabsModelCode>

/**
 * ElevenLabs realtime model constant object for autocomplete
 *
 * @example
 * \`\`\`typescript
 * import { ElevenLabsRealtimeModel } from 'voice-router-dev/constants'
 *
 * { model: ElevenLabsRealtimeModel.scribe_v2_realtime }
 * \`\`\`
 */
export const ElevenLabsRealtimeModel = {
${realtimeModels.map((m) => `  ${toIdentifier(m.id)}: "${m.id}"`).join(",\n")}
} as const satisfies Record<string, ElevenLabsRealtimeModelCode>
`

  const outputDir = path.dirname(OUTPUT_PATH)
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true })
  }

  fs.writeFileSync(OUTPUT_PATH, output, "utf-8")
  console.log(`✅ Generated ${OUTPUT_PATH}`)
  console.log(`   - ${models.length} batch models, ${realtimeModels.length} realtime models`)
}

main()
