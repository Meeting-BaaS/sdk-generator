#!/usr/bin/env node
/**
 * Generate ElevenLabs language constants
 *
 * ElevenLabs Scribe V2 supports 99 languages via ISO 639-1/639-3 codes.
 * Since the OpenAPI spec uses a free-form string for language_code (no enum),
 * the curated list lives in the tracked spec input specs/elevenlabs-languages.json
 * and is read from there. Update that file when the `elevenlabsLanguageDocs`
 * upstream is reported as changed by `pnpm providers:check-updates`.
 *
 * Run: node scripts/generate-elevenlabs-languages.js
 *
 * @see https://elevenlabs.io/docs/capabilities/speech-to-text#supported-languages
 */

const fs = require("fs")
const path = require("path")

const OUTPUT_PATH = path.join(__dirname, "../src/generated/elevenlabs/languages.ts")
const SOURCE_PATH = path.join(__dirname, "../specs/elevenlabs-languages.json")

function main() {
  console.log("📦 Generating ElevenLabs language constants...")

  if (!fs.existsSync(SOURCE_PATH)) {
    console.error(`❌ Language source not found: ${SOURCE_PATH}`)
    process.exit(1)
  }

  const LANGUAGES = JSON.parse(fs.readFileSync(SOURCE_PATH, "utf-8")).languages
  if (!Array.isArray(LANGUAGES) || LANGUAGES.length === 0) {
    console.error(`❌ ${SOURCE_PATH} has no "languages" array`)
    process.exit(1)
  }

  const output = `/**
 * Generated by scripts/generate-elevenlabs-languages.js - DO NOT EDIT MANUALLY
 *
 * ElevenLabs ScribeV2 supported languages (99 ISO 639-1/639-3 codes)
 * Run 'pnpm openapi:sync-elevenlabs-languages' to regenerate.
 *
 * @generated
 * @see https://elevenlabs.io/docs/capabilities/speech-to-text#supported-languages
 */

/**
 * ElevenLabs supported language metadata
 */
export const ElevenLabsLanguages = [
${LANGUAGES.map((lang) => `  { code: "${lang.code}", name: "${lang.name}" }`).join(",\n")}
] as const

/**
 * ElevenLabs supported language codes
 */
export const ElevenLabsLanguageCodes = [
${LANGUAGES.map((lang) => `  "${lang.code}"`).join(",\n")}
] as const

/**
 * Type for ElevenLabs language codes
 */
export type ElevenLabsLanguageCode = (typeof ElevenLabsLanguageCodes)[number]

/**
 * Language code to name mapping
 */
export const ElevenLabsLanguageLabels: Record<ElevenLabsLanguageCode, string> = {
${LANGUAGES.map((lang) => `  ${lang.code}: "${lang.name}"`).join(",\n")}
} as const

/**
 * ElevenLabs language constant object for autocomplete
 *
 * @example
 * \`\`\`typescript
 * import { ElevenLabsLanguage } from 'voice-router-dev/constants'
 *
 * { language: ElevenLabsLanguage.en }
 * { language: ElevenLabsLanguage.es }
 * \`\`\`
 */
export const ElevenLabsLanguage = {
${LANGUAGES.map((lang) => `  ${lang.code}: "${lang.code}"`).join(",\n")}
} as const satisfies Record<string, ElevenLabsLanguageCode>
`

  // Ensure output directory exists
  const outputDir = path.dirname(OUTPUT_PATH)
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true })
  }

  fs.writeFileSync(OUTPUT_PATH, output, "utf-8")
  console.log(`✅ Generated ${OUTPUT_PATH}`)
  console.log(`   - ${LANGUAGES.length} language codes`)
}

main()
